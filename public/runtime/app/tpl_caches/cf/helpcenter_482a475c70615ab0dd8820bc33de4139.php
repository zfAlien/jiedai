					<div class="clearfix f_dgray" style="padding:3px 20px">
					<div class="b">	关于p2p信贷</div>
<div>   p2p信贷(fanwe.com)是中国领先的个人对个人借贷平台(P2P lending)。p2p借贷为有资金需求和理财需求的个人搭建了一个安全、透明、稳定、高效的网络平台，以个人对个人（P2P）的方式解决借贷双方的资金需求及理财需求。用户可以在p2p借贷上获得信用评级、发布贷款请求，满足个人的资金需要；也可以把自己的闲余资金通过p2p借贷出借给信用良好的贷款者，在帮助他人的同时获得良好的资金回报率。 </div>
<div class="b">	p2p信贷的起源</div>
<div>	p2p信贷即p2p借贷，p2p借贷的另一种解释是P2P小额贷款，是一种将小额的资金聚集起来借贷给有资金需求人群的一种商业模式。由2006年“诺贝尔和平奖”得主尤努斯教授（孟加拉国）首创。20世纪90年代以来，小额信贷在世界上很多国家兴起，形成了众多发展模式，并取得了很大成功，目前，世界上比较著名的网络小额贷款服务平台有Prosper、Zopa、Lending CLub等机构。</div>
<div class="b">	p2p信贷如何收费</div>
<div>	<a href="http://127.0.0.1/p2p/index.php?ctl=aboutfee" target="_blank">见费用</a>（其它页面）</div>
<div class="b">	在p2p信贷&nbsp;平台上的借贷受法律保护么</div>
<div>	<a href="http://127.0.0.1/p2p/index.php?ctl=aboutlaws" target="_blank">见政策法规</a>（其它页面）</div>
<div class="b">p2p信贷的年利率范围是怎么规定的</div>
<div>p2p信贷目前的利率范围为6 %-24%。在p2p信贷平台上借贷的最高年利率设定为同期银行贷款年利率的4倍。且随着银行贷款利率的调整，p2p信贷&nbsp;的利率上限也将随之调整。</div>
<div>	注：</div>
<div>	1.&nbsp;p2p信贷&nbsp;的利率的调整会在商业银行贷款年利率调整后1个月内进行调整。</div>
<div>	2. 在利率调整之前的成功的借款不受调整的影响。</div>
<div class="b">	用户资料的隐私和安全如何保证</div>
<div>   保护用户的隐私和数据安全是p2p信贷&nbsp;最重要的责任之一。在传输层中,我们采用加密数据进行传输以防止网络分包被截获造成数据泄露。用户的所有的信息都储存在由防火墙保护的服务器里，这些安全防护措施都是7*24小时不间断运转的。数据根据敏感性的不同，以128位或256位的形式用SSL加密。另外，p2p信贷&nbsp;还启用了侵扰监测系统来监测那些内部和外部易受侵扰的路径，并运用其他一些人工措施来加强保护用户数据的安全。</div>
<div class="b">	我的借款会不会受p2p信贷&nbsp;运营变动而变动</div>
<div>p2p信贷及其运营公司的任何变动，均不影响已通过p2p信贷&nbsp;所达成的任何贷款协议的合法性、有效性。借款人有义务继续按照贷款协议的约定按时、足额偿还理财人的贷款本金和利息。否则理财人有权依据贷款合同的约定采取必要的法律手段追究借款人的法律责任。</div>					</div>
