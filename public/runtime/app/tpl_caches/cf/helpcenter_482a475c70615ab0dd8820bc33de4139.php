						常用名词解释                  	</div>
					<div class="clearfix f_dgray" style="padding:3px 20px">
					<div class="b">	借款人：</div>
<div>	也称贷款人，是指已经或准备在网站上进行贷款活动的用户。</div>
<div>   凡22周岁以上的中国大陆地区公民，都可以成为借款人。</div>
<div class="b">	理财人：</div>
<div>	也称投资人，是指已经或准备在网站上进行出借活动的用户。</div>
<div>   凡18周岁以上的中国大陆地区公民，都可以成为理财人。 </div>
<div class="b">	借款列表：</div>
<div>	当借款人成功发布一个借款请求时，我们将包含各种贷款信息的该请求称为一个借款列表。 </div>
<div class="b">	招标：</div>
<div>	是指一个借款人发出一个贷款请求，即创建一个借款列表的行为。 </div>
<div class="b">	投标：</div>
<div>   是指理财人将其账户内指定的金额出借给其指定的借款列表的行为。 </div>
<div class="b" id="b_2">	满标：</div>
<div>	是指一个借款列表在投标期限内足额筹集到所需资金。 </div>
<div class="b">	放款：</div>
<div>	指一个借款列表满标后且已符合放款标准，p2p借贷将把所筹资金打入借款人在p2p借贷的账户中。即成功贷款。</div>
<div class="b">	流标：</div>
<div>	是指一个借款列表的投标期限已过，但是贷款没有足额筹集齐，即贷款失败。 </div>
<div class="b">	投标金额：</div>
<div>	是指理财人对借款列表进行投标的金额。</div>
<div class="b">	帐户总额：</div>
<div>	指用户账户的所有金额（含冻结金额和可用金额）。</div>
<div class="b">	可用金额：</div>
<div>	是指用户可自由支配的金额。</div>
<div class="b">	冻结金额：</div>
<div>	用户对某借款列表进行投标后，在此列表的招标期结束之前，这部分投标金额将被锁定，“冻结金额”是指因这类型锁定金额的总额。如果列表放款，这部分金额将转给该列表的借款人；如果该列表流标，这部分金额将立即变为用户的可用金额。 </div>
<div class="b" id="b_1">	等额本息：</div>
<div>	定义：等额本息还款法是一种被广泛采用的还款方式，在还款期内，每月偿还同等数额的贷款(包括本金和利息)。借款人每月还款额中的本金比重逐月递增、利息比重逐月递减。 </div>
<div>	其计算公式如下：</div>
<div>	每月还款额=[贷款本金×月利率×（1+月利率）^还款总期数]÷[（1+月利率）^还款总期数-1]<br />
因计算中存在四舍五入，最后一期还款金额与之前略有不同。</div>
<div class="b">	什么是加权收益率</div>
<div>	由于借款时间不定，p2p借贷采用加权平均的方式计算借出者的收益率，具体公式如下：</div>
<div>	加权收益率 = (本金1*收益率1+本金2*收益率2+本金3*收益率3 + ......本金n*收益率n-坏账计提+罚息)/(本金1+本金2+本金3 + ......本金n)</div>
<div>	注：</div>
<div>	1. 此公式未计算可能的资金闲置期</div>					</div>
